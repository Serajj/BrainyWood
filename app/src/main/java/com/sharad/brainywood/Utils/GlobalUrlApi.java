package com.sharad.brainywood.Utils;

public class GlobalUrlApi {

    private String UrlAppFolder;
    private String BaseUrl;

    public GlobalUrlApi() {
        UrlAppFolder ="https://www.brainywoodindia.com/app/";
        BaseUrl ="https://www.brainywoodindia.com/";
    }

    public String getUrlAppFolder() {
        return UrlAppFolder;
    }

    public String getBaseUrl() {
        return BaseUrl;
    }


}
